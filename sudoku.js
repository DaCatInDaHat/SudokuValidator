function validSolution(board) {
  let rows = Array.from(Array(9), () => []);
  let columns = Array.from(Array(9), () => []);
  let boxes = Array.from(Array(9), () => []);

  for (let row in board) {
    for (let column in board) {
      let cell = board[row][column];

      if (cell !== 0) {
        if (rows[row].includes(cell)) return false;
        else rows[row].push(cell);

        if (columns[column].includes(cell)) return false;
        else columns[column].push(cell);

        let boxIndex = Math.floor(row / 3) * 3 + Math.floor(column / 3);

        if (boxes[boxIndex].includes(cell)) return false;
        else boxes[boxIndex].push(cell);
      } else return false;
    }
  }

  return true;
}

